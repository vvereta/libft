#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	unsigned int	len1;
	unsigned int	len2;
	char			*result;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	if (!(result = ft_strnew(len1 + len2)))
		return (NULL);
	ft_strncpy(result, s1, len1);
	ft_strncat(result, s2, len2);
	return (result);
}
